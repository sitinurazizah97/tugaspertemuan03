package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan = 4;
        switch (jalanJalan) {
            case 1:
                System.out.println("Urang Lapas");
                break;
            case 2:
                System.out.println("Urang acayan");
                break;
            case 3:
                System.out.println("Ubat Nyamuk");
                break;
            case 4:
                System.out.println("dubil dit");
                break;
            case 5:
                System.out.println("mahluk halus");
                break;
        }
    }
}